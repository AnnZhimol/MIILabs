class Product:
    def __init__(self, name, cost, calories, cholesterol, sucrose, dietary_fiber):
        self.name=name                      #название
        self.cost=cost                      #стоимость (руб.)
        self.calories=calories              #калории (ккал)
        self.cholesterol=cholesterol        #холестерин (мг)
        self.sucrose=sucrose                #сахароза (г)
        self.dietary_fiber=dietary_fiber    #пищевые волокна (г)

